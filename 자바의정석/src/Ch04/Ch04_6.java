package Ch04;

public class Ch04_6 {
	
	// 두 개의 주사위를 던졌을 때 눈의 합이 6이 되는 모든 경우의 수
	public static void main(String[] args) {
		int count = 0;
		for (int i = 1; i <= 6; i++) {
			for (int j = 1; j <= 6; j++) {
				if(i + j == 6) {
					System.out.println("(" + i + ", " + j + ")");
					count++;
				}
			}
		}
		System.out.println("count : " + count);
	}

}
